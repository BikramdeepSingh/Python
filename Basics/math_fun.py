'''
Math functions in python
'''

import math #it imports math module. now math is an object like a string
# now we can access the methods of math using . operator

x=2.9
print(round(x)) #returns rounded value
print(abs(-5)) #retuns absolute value which is always positive
#for complex mathematical calculations
print(math.ceil(2.1)) #returns upper rounded value i.e. 3
print(math.floor(2.1))  #returns lower rounded value i.e. 2
print(math.factorial(3)) #returns factorial of passed value and error if value is not an integer or negative